package com.cubrid.cubridmanager.ui.monitoring.editor.count;

/**
 * @author lcl
 * @version 1.1 - 2009-9-10 created by lcl
 */
class ColumnData {
	int count; // the count is zero, means the slot never set.
	long min;
	long max;
	long sum;
	long last;

	public boolean isInvalid() {
		return count == 0;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public long getMin() {
		return min;
	}

	public void setMin(long min) {
		this.min = min;
	}

	public long getMax() {
		return max;
	}

	public void setMax(long max) {
		this.max = max;
	}

	public long getSum() {
		return sum;
	}

	public void setSum(long sum) {
		this.sum = sum;
	}

	public long getLast() {
		return last;
	}

	public void setLast(long last) {
		this.last = last;
	}
}
